package com.abhinandita.task5;

public class Stage2_items {
    int img1;
    String text;
    int img2;

    public Stage2_items(int img1, String text, int img2) {
        this.img1 = img1;
        this.text = text;
        this.img2 = img2;
    }

    public int getImg1() {
        return img1;
    }

    public void setImg1(int img1) {
        this.img1 = img1;
    }

    public String getText() {
        return text;
    }

    public void setText(String text) {
        this.text = text;
    }

    public int getImg2() {
        return img2;
    }

    public void setImg2(int img2) {
        this.img2 = img2;
    }
}
